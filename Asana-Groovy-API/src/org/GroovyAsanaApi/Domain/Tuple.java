package org.GroovyAsanaApi.Domain;

public class Tuple {

	private String id;
	private String name;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return "tuple [id=" + id + ", name=" + name + "]";
	}
}
